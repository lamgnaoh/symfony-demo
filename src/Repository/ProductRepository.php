    public function findByName(string $name): array
    {
        //        return $this->findBy(['name' => $name]);
        return $this->createQueryBuilder('p')
            ->andWhere('p.name like :name')
            ->setParameter('name', '%'.$name.'%')
            ->orderBy('p.name', 'ASC')
